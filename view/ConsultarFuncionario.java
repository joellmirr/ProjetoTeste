    public void SetarLinha(){
        
        int setar = jTable_funcionario.getSelectedRow();
        jTextField_SetarCcodigo.setText(jTable_funcionario.getModel().getValueAt(setar,0).toString());
        jTextField_SetarNome.setText(jTable_funcionario.getModel().getValueAt(setar,1).toString());
        jTextField_setarCargo.setText(jTable_funcionario.getModel().getValueAt(setar,2).toString());
        jTextField_setarCpf.setText(jTable_funcionario.getModel().getValueAt(setar,3).toString());
        jTextField_setarEmail.setText(jTable_funcionario.getModel().getValueAt(setar,4).toString());
        jTextField_setarTelefone.setText(jTable_funcionario.getModel().getValueAt(setar,5).toString());
        jTextField_setarEndereco.setText(jTable_funcionario.getModel().getValueAt(setar,6).toString());
        jTextField_setarNivel.setText(jTable_funcionario.getModel().getValueAt(setar,7).toString());
        
    }
    
    public void Alterar(){
        
        Funcionario f = new Funcionario();
            f.setNome(jTextField_SetarNome.getText());
            f.setCargo(jTextField_setarCargo.getText());
            f.setCpf(jTextField_setarCpf.getText());
            f.setEmail(jTextField_setarEmail.getText());
            f.setTelefone(jTextField_setarTelefone.getText());
            f.setEndereco(jTextField_setarEndereco.getText());
            f.setNivelAscesso(jTextField_setarNivel.getText());
            f.setCodigo(jTextField_SetarCcodigo.getText());
        
        fdao.atualizar(f);
        
        int i = jTable_funcionario.getSelectedRow();
        DefaultTableModel taFun = (DefaultTableModel) jTable_funcionario.getModel();
        
        if(i >= 0){
            
            taFun.setValueAt(jTextField_SetarCcodigo.getText(), i, 0);
            taFun.setValueAt(jTextField_SetarNome.getText(), i, 1);
            taFun.setValueAt(jTextField_setarCargo.getText(), i, 2);
            taFun.setValueAt(jTextField_setarCpf.getText(), i, 3);
            taFun.setValueAt(jTextField_setarEmail.getText(), i, 4);
            taFun.setValueAt(jTextField_setarTelefone.getText(), i, 5);
            taFun.setValueAt(jTextField_setarEndereco.getText(), i, 6);
            taFun.setValueAt(jTextField_setarNivel.getText(), i, 7);
            
            JOptionPane.showMessageDialog(null,"Realizado Atualização!");
           
            this.Limpar();
            
        }else{
            JOptionPane.showMessageDialog(null,"Erro Atualização");
        }
        
    }
                
    public void Excluir() {
        
        int i = jTable_funcionario.getSelectedRow();
        
        int f = Integer.parseInt(jTextField_SetarCcodigo.getText());
        
        fdao.excluir(f);
        
        if(i >= 0){
            DefaultTableModel taFun = (DefaultTableModel) jTable_funcionario.getModel();
            taFun.setNumRows(i);
        
            JOptionPane.showMessageDialog(null,"Item Excluido!");
           
             this.Limpar();
        
        }else{
            JOptionPane.showMessageDialog(null,"Erro ao Excluir!\nSelesione Item!");
        }
    }
