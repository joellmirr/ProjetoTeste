    public void Buscar(){
        Funcionario f = new Funcionario();
        ArrayList<Funcionario> listaFu = new ArrayList<>();
        //criar um modelo de tabela
        DefaultTableModel taFun = (DefaultTableModel) jTable_funcionario.getModel();
        taFun.setNumRows(0);
        
        listaFu = fdao.Buscar(jTextField_nomefuncionario_.getText());
        
       Object colunas[] = new Object[8];
        for(int i=0; i<listaFu.size();i++){
            f = listaFu.get(i);
            colunas[0] = f.getCodigo();
            colunas[1] = f.getNome();
            colunas[2] = f.getCargo();
            colunas[3] = f.getCpf();
            colunas[4] = f.getEmail();
            colunas[5] = f.getTelefone();
            colunas[6] = f.getEndereco();
            colunas[7] = f.getNivelAscesso();
            taFun.addRow(colunas);
        }
    }
    
