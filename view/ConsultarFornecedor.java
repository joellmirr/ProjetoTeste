    public void Buscar(){
        Fornecedor f = new Fornecedor();
        ArrayList<Fornecedor> listaFo = new ArrayList<>();
        //criar um modelo de tabela
        DefaultTableModel taFornecedor = (DefaultTableModel) jTable_fornecedor.getModel();
        taFornecedor.setNumRows(0);
        
        listaFo = fdao.Buscar(jTextField_nomeFornecedor_.getText());
        
        Object colunas[] = new Object[7];
        for(int i=0;i<listaFo.size();i++){
            f = listaFo.get(i);
            colunas[0] = f.getCodigo();
            colunas[1] = f.getNome();
            colunas[2] = f.getCnpj();
            colunas[3] = f.getEmail();
            colunas[4] = f.getTelefone();
            colunas[5] = f.getProduto();
            colunas[6] = f.getEndereco();
            taFornecedor.addRow(colunas);
        }
    }
    
