    public void SetarLinha(){
        
        int setar = jTable_fornecedor.getSelectedRow();
        jTextField_setarCodigo.setText(jTable_fornecedor.getModel().getValueAt(setar,0).toString());
        jTextField_setarNome.setText(jTable_fornecedor.getModel().getValueAt(setar,1).toString());
        jTextField_setarCnpj.setText(jTable_fornecedor.getModel().getValueAt(setar,2).toString());
        jTextField_setarEmail.setText(jTable_fornecedor.getModel().getValueAt(setar,3).toString());
        jTextField_setarTelefone.setText(jTable_fornecedor.getModel().getValueAt(setar,4).toString());
        jTextField_setarEndereco.setText(jTable_fornecedor.getModel().getValueAt(setar,5).toString());
        jTextField_setarPoduto.setText(jTable_fornecedor.getModel().getValueAt(setar,6).toString());
        
    }
   
    public void Alterar(){
        
        Fornecedor f = new Fornecedor();
            f.setNome(jTextField_setarNome.getText());
            f.setCnpj(jTextField_setarCnpj.getText());
            f.setEmail(jTextField_setarEmail.getText());
            f.setTelefone(jTextField_setarTelefone.getText());
            f.setProduto(jTextField_setarPoduto.getText());
            f.setEndereco(jTextField_setarEndereco.getText());
            f.setCodigo(jTextField_setarCodigo.getText());
            
        fdao.atualizar(f);
        
        int i = jTable_fornecedor.getSelectedRow();
        DefaultTableModel taFornecedor = (DefaultTableModel) jTable_fornecedor.getModel();
        
        if(i >= 0){
            
            taFornecedor.setValueAt(jTextField_setarCodigo.getText(), i, 0);
            taFornecedor.setValueAt(jTextField_setarNome.getText(), i, 1);
            taFornecedor.setValueAt(jTextField_setarCnpj.getText(), i, 2);
            taFornecedor.setValueAt(jTextField_setarEmail.getText(), i, 3);
            taFornecedor.setValueAt(jTextField_setarTelefone.getText(), i, 4);
            taFornecedor.setValueAt(jTextField_setarPoduto.getText(), i, 5);
            taFornecedor.setValueAt(jTextField_setarEndereco.getText(), i, 6);
            
            JOptionPane.showMessageDialog(null,"Realizado Atualização!");
           
            this.Limpar();
            
        }else{
            JOptionPane.showMessageDialog(null,"Erro Atualização");
        }
            
    }
    
     public void Excluir() {
        
        int i = jTable_fornecedor.getSelectedRow();
        
        int f = Integer.parseInt(jTextField_setarCodigo.getText());
        
        fdao.excluir(f);
        
        if(i >= 0){
            DefaultTableModel taFun = (DefaultTableModel) jTable_fornecedor.getModel();
            taFun.setNumRows(i);
        
            JOptionPane.showMessageDialog(null,"Item Excluido!");
           
             this.Limpar();
        
        }else{
            JOptionPane.showMessageDialog(null,"Erro ao Excluir!\nSelesione Item!");
        }
    }

